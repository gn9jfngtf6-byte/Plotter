function toggleLangMenu() {
  const menu = document.getElementById('lang-menu');
  if (!menu) return;
  const inner = document.getElementById('lang-menu-inner');
  if (menu.style.display === 'none' || !menu.style.display) {
    // Populate buttons
    inner.innerHTML = '';
    LANG_ORDER.forEach(code => {
      const L = LANGUAGES[code];
      const label = (L?.name || code.toUpperCase());
      const btn = document.createElement('button');
      btn.textContent = (code === currentLang ? '✓ ' : '  ') + label;
      btn.style.cssText = 'display:block;width:100%;text-align:left;background:none;border:none;padding:5px 10px;cursor:pointer;font-size:12px;color:var(--text);border-radius:4px;';
      btn.onmouseenter = () => btn.style.background = 'var(--bg-hover,rgba(128,128,128,0.15))';
      btn.onmouseleave = () => btn.style.background = 'none';
      btn.onclick = (e) => { e.stopPropagation(); setLang(code); closeLangMenu(); };
      inner.appendChild(btn);
    });
    // Position below the lang-btn
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
      const r = langBtn.getBoundingClientRect();
      menu.style.top = (r.bottom + 4) + 'px';
      menu.style.left = (r.left) + 'px';
    }
    menu.style.display = 'block';
    // Close on outside click
    setTimeout(() => { document.addEventListener('click', closeLangMenuOutside, { once: true }); }, 0);
  } else {
    closeLangMenu();
  }
}
function closeLangMenu() {
  const menu = document.getElementById('lang-menu');
  if (menu) menu.style.display = 'none';
  document.removeEventListener('click', closeLangMenuOutside);
}
function closeLangMenuOutside(e) {
  const menu = document.getElementById('lang-menu');
  if (menu && !menu.contains(e.target)) closeLangMenu();
}
function setLang(code) {
  currentLang = code;
  applyLanguage();
  const btn = document.getElementById('lang-btn');
  if (btn) btn.textContent = '🌐 ' + (LANGUAGES[code]?.code || code.toUpperCase());
}

function applyLanguage() {
  const L = LANGUAGES[currentLang] || LANGUAGES.de;

  // Panel titles (Pfeil-Span beibehalten)
  document.querySelectorAll('.panel-title[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!L[key]) return;
    Array.from(el.childNodes).forEach(n => { if (n.nodeType === 3) n.remove(); });
    el.insertBefore(document.createTextNode(L[key]), el.firstChild);
  });

  // Alle anderen Elemente mit data-i18n (Buttons, Spans, Labels, Divs, Options)
  document.querySelectorAll('[data-i18n]:not(.panel-title)').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!L[key]) return;
    // Bei Buttons mit Emoji-Prefix: nur Text nach dem ersten Leerzeichen ersetzen
    if (el.tagName === 'BUTTON') {
      const txt = L[key];
      // Emoji-Präfix beibehalten wenn vorhanden (📷, 💾, 📍, 🗑, ✏, 📋)
      el.textContent = txt;
    } else {
      el.textContent = L[key];
    }
  });

  // Placeholder-Attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (L[key]) el.placeholder = L[key];
  });

  // Title-Attribute
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (L[key]) el.title = L[key];
  });

  // Hint-Text
  const hint = document.getElementById('linear-hint');
  if (hint) hint.textContent = L.linear_hint || '';

  // area-toggle-btn Text (kann durch toggleArea() überschrieben werden)
  const areaBtn = document.getElementById('area-toggle-btn');
  if (areaBtn && areaBtn.textContent === (LANGUAGES.de.btn_area || 'Fläche anzeigen') ||
      areaBtn && areaBtn.getAttribute('data-i18n') === 'btn_area') {
    if (!areaBtn.classList.contains('active-btn') && L.btn_area) areaBtn.textContent = L.btn_area;
  }
}

